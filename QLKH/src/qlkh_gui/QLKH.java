    private void tblKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKHMouseClicked
        // TODO add your handling code here:
        int row = tblKH.getSelectedRow();
        txtMaKH.setText(String.valueOf(tblKH.getValueAt(row, 0)));
        txtTenKH.setText(String.valueOf(tblKH.getValueAt(row, 1)));
        txtCMND.setText(String.valueOf(tblKH.getValueAt(row, 2)));
        // Lấy và hiển thị ngày sinh
        Date ngaySinh = (Date) tblKH.getValueAt(row, 3);
        DateNgaySinh.setDate(ngaySinh);
        // Lấy và hiển thị giới tính
        String gioiTinh = String.valueOf(tblKH.getValueAt(row, 4));
        ComboboxGioiTinh.setSelectedItem(gioiTinh);
        txtSDT.setText(String.valueOf(tblKH.getValueAt(row, 5)));
    }//GEN-LAST:event_tblKHMouseClicked

