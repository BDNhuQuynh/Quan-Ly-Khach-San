    private void fillToTable() {
        dsKH = QLKHDAO.dsKhachHang();
        DefaultTableModel dtm = (DefaultTableModel) tblKH.getModel();
        dtm.setRowCount(0);
        for (QLKHPOJO g : dsKH) {
            Vector<Object> vec = new Vector<>();
            vec.add(g.getMaKH());
            vec.add(g.getTenKH());
            vec.add(g.getCMND());
            vec.add(g.getNgaySinh());
            vec.add(g.getGioiTinh());
            vec.add(g.getSDT());
            dtm.addRow(vec);
        }
    }
    
    private void clearForm(){
        txtMaKH.setText("");
        txtTenKH.setText("");
        txtCMND.setText("");
        txtSDT.setText("");
    }
    
    private void fillForm(){
        index =tblKH.getSelectedRow();
        QLKHPOJO g=dsKH.get(index);
        txtMaKH.setText(g.getMaKH()+"");
        txtTenKH.setText(g.getTenKH()+"");
        txtCMND.setText(g.getCMND()+"");
        DateNgaySinh.setDate(g.getNgaySinh());
        ComboboxGioiTinh.setSelectedItem(g.getGioiTinh());
        txtSDT.setText(g.getSDT()+"");
    }
    
     


