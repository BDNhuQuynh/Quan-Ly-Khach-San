    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String gt = ComboboxGioiTinh.getSelectedItem().toString();
        QLKHPOJO kh = new QLKHPOJO();
        kh.setTenKH(txtTenKH.getText());
        kh.setCMND(txtCMND.getText());
        java.util.Date ngaySinhUtil = DateNgaySinh.getDate();
        java.sql.Date ngaySinhSql = new java.sql.Date(ngaySinhUtil.getTime());
        kh.setNgaySinh(ngaySinhSql);
        kh.setGioiTinh(gt);
        kh.setSDT(txtSDT.getText());
        QLKHDAO.themKhachHang(kh);
        fillToTable();
        clearForm();
    }//GEN-LAST:event_jButton1ActionPerformed

